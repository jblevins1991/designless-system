export type CommonInputType = {
  /**
   * Makes the element non-interactive.
   *
   * Default value: false
   */
  disabled?: boolean;
  /**
   * A string representing the error text.
   */
  error?: string;
  /**
   * A string representing the hint text.
   */
  hint?: string;
  /**
   * A string representing the label text.
   */
  label: string;
  /**
   * The name of the input component.
   */
  name: string;
};
